// src/utils/pdf-generator.js - PDF Generation
export function generatePDF(elementId, filename = 'scorecard.pdf') {
  const element = document.getElementById(elementId);
  if (!element) {
    console.error('Element not found');
    return;
  }
  
  // Check if html2pdf is available
  if (typeof html2pdf === 'undefined') {
    alert('PDF library not loaded. Please check your internet connection.');
    return;
  }
  
  const opt = {
    margin: 10,
    filename: filename,
    image: { type: 'jpeg', quality: 0.98 },
    html2canvas: { 
      scale: 2, 
      useCORS: true,
      backgroundColor: '#0A0A0A'
    },
    jsPDF: { 
      unit: 'mm', 
      format: 'a4', 
      orientation: 'portrait' 
    },
    pagebreak: { mode: ['avoid-all', 'css', 'legacy'] }
  };
  
  html2pdf().set(opt).from(element).save();
}

export function generateScorecardPDF(matchData, filename = 'scorecard.pdf') {
  // Create a temporary container
  const container = document.createElement('div');
  container.id = 'pdfContainer';
  container.style.cssText = `
    background: #0A0A0A;
    color: #FFFFFF;
    padding: 30px;
    font-family: 'Poppins', sans-serif;
    max-width: 800px;
    margin: 0 auto;
  `;
  
  container.innerHTML = `
    <div style="text-align: center; margin-bottom: 20px;">
      <h1 style="color: #FFD700;">🏏 ${matchData.title || 'Match Scorecard'}</h1>
      <p style="color: #B0B0B0;">${new Date(matchData.date).toLocaleString()}</p>
    </div>
    <div style="background: #1A1A1A; padding: 16px; border-radius: 12px; border: 2px solid #FFD700; margin-bottom: 20px;">
      <div style="display: flex; justify-content: space-between;">
        <span style="color: #B0B0B0;">${matchData.team1Name} vs ${matchData.team2Name}</span>
        <span style="color: #FFD700; font-weight: bold;">${matchData.result || 'Match in progress'}</span>
      </div>
    </div>
    ${matchData.battingStats ? `
      <div style="margin-bottom: 16px;">
        <h3 style="color: #FFD700;">Batting</h3>
        <table style="width: 100%; border-collapse: collapse; color: #FFFFFF;">
          <thead>
            <tr style="border-bottom: 2px solid #FFD700;">
              <th style="text-align: left; padding: 8px;">Player</th>
              <th style="text-align: right; padding: 8px;">R</th>
              <th style="text-align: right; padding: 8px;">B</th>
              <th style="text-align: right; padding: 8px;">4s</th>
              <th style="text-align: right; padding: 8px;">6s</th>
              <th style="text-align: right; padding: 8px;">SR</th>
            </tr>
          </thead>
          <tbody>
            ${matchData.battingStats.map(b => `
              <tr style="border-bottom: 1px solid rgba(255,255,255,0.05);">
                <td style="padding: 8px;">${b.name}</td>
                <td style="text-align: right; padding: 8px; font-weight: bold;">${b.runs}</td>
                <td style="text-align: right; padding: 8px;">${b.balls}</td>
                <td style="text-align: right; padding: 8px;">${b.fours}</td>
                <td style="text-align: right; padding: 8px;">${b.sixes}</td>
                <td style="text-align: right; padding: 8px;">${b.strikeRate}</td>
              </tr>
            `).join('')}
          </tbody>
        </table>
      </div>
    ` : ''}
    ${matchData.bowlingStats ? `
      <div style="margin-bottom: 16px;">
        <h3 style="color: #FFD700;">Bowling</h3>
        <table style="width: 100%; border-collapse: collapse; color: #FFFFFF;">
          <thead>
            <tr style="border-bottom: 2px solid #FFD700;">
              <th style="text-align: left; padding: 8px;">Bowler</th>
              <th style="text-align: right; padding: 8px;">O</th>
              <th style="text-align: right; padding: 8px;">M</th>
              <th style="text-align: right; padding: 8px;">R</th>
              <th style="text-align: right; padding: 8px;">W</th>
              <th style="text-align: right; padding: 8px;">ECO</th>
            </tr>
          </thead>
          <tbody>
            ${matchData.bowlingStats.map(b => `
              <tr style="border-bottom: 1px solid rgba(255,255,255,0.05);">
                <td style="padding: 8px;">${b.name}</td>
                <td style="text-align: right; padding: 8px;">${b.overs}</td>
                <td style="text-align: right; padding: 8px;">${b.maidens}</td>
                <td style="text-align: right; padding: 8px;">${b.runs}</td>
                <td style="text-align: right; padding: 8px; font-weight: bold;">${b.wickets}</td>
                <td style="text-align: right; padding: 8px;">${b.economy}</td>
              </tr>
            `).join('')}
          </tbody>
        </table>
      </div>
    ` : ''}
    <div style="text-align: center; color: #B0B0B0; font-size: 12px; margin-top: 20px;">
      Generated by Pandavas Cricket Club Scorer v3.1
    </div>
  `;
  
  document.body.appendChild(container);
  generatePDF('pdfContainer', filename);
  
  // Clean up
  setTimeout(() => {
    document.body.removeChild(container);
  }, 1000);
}
